public class arraymul {
    public static void main(String[] args) {
        //int sum=0;
        int mult=2;

        int[] k={2,3,45,5};
        System.out.println(k.length);
        for (int i=0; i<=k.length-1;i++)
        {
            //sum=sum+k[i];
            mult=mult*k[i];
            System.out.println(mult);
        }


    }
}
